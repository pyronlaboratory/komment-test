class Api {
/**
* @description This function logs the message "API is running live!" to the console.
* 
* @returns { void } The output returned by this function is "API is running live!"
* because the `console.log()` statement inside the function will print that message
* to the console.
*/
  public static run(): void {
    console.log("API is running live!");
  }
}




Api.run();

