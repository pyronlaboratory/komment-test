class Api {
/**
* @description This function simply logs the message "API is running live!" to the
* console.
* 
* @returns { void } The output returned by this function is:
* 
* "API is running live!"
* 
* This is because the `console.log()` statement within the function calls the browser's
* console and logs the specified string as an output.
*/
  public static run(): void {
    console.log("API is running live!");
  }
}

Api.run();