def server(port):
  """
  The `server` function listens for incoming requests on a given port and prints
  a message indicating that the server is active and listening on that port.

  Args:
      port (int): The `port` input parameter is passed as an argument to the
          `server()` function and sets the port number that the server will listen
          on.

  """
  print(f"Server listening on port: {port}")
