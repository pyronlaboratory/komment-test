def server(port):

  """
  The `server` function listens for incoming requests on a given port and prints
  a message indicating that it is ready to serve requests.

  Args:
      port (int): The `port` input parameter passes a value to the `print()`
          function inside the `server()` function so it can be included as a string
          inside the printed message.

  """
  print(f"Server listening on port: {port}")
