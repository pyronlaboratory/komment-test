def server(port):
  """
  This function defines a server that listens for incoming requests on a specific
  port (as determined by the `port` parameter passed to the function).

  Args:
      port (int): The `port` input parameter passes a value to the function that
          the function uses to print a message stating which port the server is
          currently using.

  """
  print(f"Server listening on port: {port}")
