def server(port):
  """
  The function `server(port)` prints a message indicating that the server is
  listening on the specified port `port`.

  Args:
      port (int): The `port` input parameter passes a value to the function and
          this value will be included when the string is created. Inside the
          function `print()` will be passed a new string formed with this function
          call and it will print "Server listening on port: {port}".

  """
  print(f"Server listening on port: {port}")
