def server(port):
  """
  This function takes a single argument `port` and prints a message indicating
  that the server is listening on that port. The message also mentions that new
  changes will be added to the system while the output channel is being switched
  to a different mode. In other words - this code will print a message saying that
  the server is listening for incoming requests and it's doing something with the
  incoming requests.

  Args:
      port (int): The `port` input parameter is used to specify the port number
          on which the server should listen for incoming connections.

  """
  print(f"Server listening: {port} | adding new changes in parallel while output channel mode changed")
